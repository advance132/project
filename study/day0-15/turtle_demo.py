import turtle
#画一个正方形
turtle.pensize(4)#设置画笔的宽度
turtle.pencolor('red')#设置画笔的颜色

turtle.forward(100)
turtle.right(90)
turtle.forward(100)
turtle.right(90)
turtle.forward(100)
turtle.right(90)
turtle.forward(100)


