#计算幂指数
'''
求幂指数
将指数 n 以二进制形式分解，把 xⁿ 拆分为多个 x^(2^k) 的乘积，从而将 n 次乘法运算压缩为 log₂n 次。
示例：计算 3⁵
指数 5 的二进制为 101，即 5 = 4 + 1 = 2² + 2⁰
因此 3⁵ = 3^(2²) × 3^(2⁰) = 9² × 3¹ = 81 × 3 = 243
'''
x = int(input("请输入指数: "))
n = int(input("请输入幂: "))
result = 1
if n<0 :
    n = -n
    x = 1/x
    
while (n>0):
    if n & 1:#按位与运算，判断 n 的二进制最后一位是否为 1
        result *= x
    x *= x
    n >>= 1#按位右移运算，等价于 n = Math.floor(n / 2)，去掉已经处理的最后一位

print(result);


